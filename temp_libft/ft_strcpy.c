#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*arr;

	arr = dst;
	while (*src)
	{
		*arr = *src;
		++src;
		++arr;
	}
	*arr = '\0';
	return (dst);
}
